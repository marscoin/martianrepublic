<h3>Four Persistent Challenges</h3>

<p>The empirical literature identifies four persistent challenges for direct democracy:</p>

<ul>
<li><strong>Rational ignorance</strong> (Anthony Downs, 1957): since any individual vote has negligible impact, voters invest little in acquiring information.</li>
<li><strong>Voter fatigue:</strong> California's ballot proposition system regularly presents 10+ measures per election, with research showing voter drop-off on lower-ballot items.</li>
<li><strong>Tyranny of the majority:</strong> between 1998 and 2012, voters in 30 U.S. states approved same-sex marriage bans.</li>
<li><strong>Manipulation:</strong> the 2020 California Proposition 22 saw Uber, Lyft, and DoorDash spend over <strong>$200 million</strong> to pass a measure the legislature had rejected.</li>
</ul>

<p>These challenges are not arguments against direct democracy&mdash;they are specifications for its design safeguards.</p>

