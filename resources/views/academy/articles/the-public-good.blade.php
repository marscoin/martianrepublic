<h3>Selectorate Theory</h3>

<p><strong>Selectorate theory</strong> (Bueno de Mesquita and Smith, <em>The Logic of Political Survival</em>, 2003) provides the most powerful modern framework. All political systems contain three groups: the nominal selectorate (those with some formal say), the real selectorate (those who actually participate), and the winning coalition (the minimum group whose support keeps the leader in power). When the winning coalition is small (dictatorships), leaders use private goods&mdash;bribes, patronage&mdash;to maintain support. When it is large (democracies), leaders must provide public goods because individually buying off millions is too expensive.</p>

<div class="callout mars-red">
<p><strong>The design implication for Mars: make the winning coalition as large as possible relative to the selectorate.</strong> Direct democracy maximizes this ratio. This is not idealism&mdash;it is the mathematical prediction of selectorate theory for maximizing public goods provision.</p>
</div>

<p>Jason Brennan's <em>Against Democracy</em> (2016) proposed <strong>epistocracy</strong>&mdash;rule by the knowledgeable&mdash;arguing most voters are "hobbits" (apathetic) or "hooligans" (partisan and irrational). The fundamental objection is devastating: <strong>who tests the testers?</strong> Any knowledge test embeds value judgments. Jeffrey Friedman's research shows political knowledge correlates with ideological dogmatism&mdash;the "knowledgeable" are often the most biased, not the most rational. For Mars, the answer is not epistocracy but <em>epistemic democracy</em>&mdash;designing institutions that harness cognitive diversity through sortition and structured deliberation, as Ireland has demonstrated.</p>

