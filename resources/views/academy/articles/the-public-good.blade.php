<p>Radiation requires underground habitats or regolith shielding. There can be no externalities&mdash;all consequences are internalized within the sealed habitat. Ostrom's eight design principles were developed for exactly this type of commons, and her work on polycentric governance&mdash;multiple overlapping decision centers at different scales&mdash;provides the organizational template.</p>

<h3>The Exit Impossibility</h3>

<blockquote>
<p>When exit is impossible, voice must be maximized&mdash;or the system becomes tyrannical.</p>
<div class="attribution">&mdash; derived from Albert Hirschman, <em>Exit, Voice, and Loyalty</em>, 1970</div>
</blockquote>

<p><strong>The exit impossibility</strong> is the single most consequential constraint for governance design. Albert Hirschman's <em>Exit, Voice, and Loyalty</em> (1970) established that organizations respond to decline through three mechanisms: exit (leave), voice (protest and reform from within), and loyalty (stay despite dissatisfaction). On Mars, exit is effectively impossible&mdash;travel windows occur only every 26 months, the journey takes 6+ months, and return infrastructure may not exist. This eliminates the most fundamental check on governance failure. The Martian Republic's commitment to direct democratic participation, multiple proposal pathways, constitutional amendment rights, and guaranteed citizen voice is not optional idealism&mdash;it is structural necessity dictated by orbital mechanics.</p>

<h3>Emergency Powers and Privacy</h3>

<p><strong>Emergency decision-making versus deliberation</strong> is the core operational tension. When a habitat breach occurs, there cannot be a referendum. The Roman dictator model provides the template: temporary emergency power, pre-authorized by constitutional process, strictly scoped to the specific crisis, with an automatic time limit and mandatory post-crisis accountability. Cincinnatus (458 BCE) resolved his crisis and returned to farming within 16 days. The failure mode&mdash;Sulla and Caesar transforming temporary authority into permanent autocracy&mdash;demonstrates why the constitutional encoding of emergency powers must include cryptographic sunset clauses.</p>

<div class="callout">
<p><strong><i class="fa-solid fa-lock" style="color:var(--mr-cyan); margin-right:6px;"></i> Privacy in a Community of 150.</strong> The Marscoin approach uses the CoinShuffle protocol for ballot mixing: citizens join shuffle sessions where civic wallet addresses participate in a mixing transaction, producing untraceable 0.1 MARS "ballot coins" on fresh addresses. Votes are cast by spending ballot coins with embedded OP_RETURN metadata. The result: <strong>who participated is public, how they voted is private</strong>&mdash;end-to-end auditable while preserving ballot secrecy. As the colony grows, zero-knowledge proofs can provide stronger cryptographic privacy guarantees.</p>
</div>

<p><strong>AI in governance</strong> should serve as an advisory and procedural tool, never a decision-maker. All AI models used in governance must be open-source, auditable, and multiple competing analyses should prevent any single system from becoming authoritative. The University of Colorado Law Review documented different LLMs reaching opposite conclusions on the same constitutional question&mdash;a reminder that AI "judgment" is probabilistic, not deterministic. The constitutional constraint must be encoded: AI advises, humans decide.</p>

