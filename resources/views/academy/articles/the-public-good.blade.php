<h2>II. Direct Democracy: 2,500 Years of Evidence</h2>

<p>Athens's direct democracy operated at remarkable scale. By the 4th century BCE, the ekklesia met <strong>40 times per year</strong>. A quorum of 6,000 was required for major decisions. Voting was primarily by show of hands, with officials judging outcomes by sight. The <strong>graphe paranomon</strong>&mdash;introduced c. 415 BCE&mdash;allowed any citizen to indict the proposer of an unconstitutional decree, functioning as a form of judicial review centuries before Marbury v. Madison. If convicted, the proposer faced ruinous fines; three convictions meant total loss of political rights (atimia). Mogens Herman Hansen estimates this mechanism was invoked roughly once per month, producing approximately <strong>800 cases</strong> between 403 and 322 BCE. It was a remarkably sophisticated safeguard, directly relevant to the Martian Republic's tiered proposal system.</p>

<div class="callout green">
<p><strong><i class="fa-solid fa-mountain-sun" style="color:var(--mr-green); margin-right:6px;"></i> Switzerland: The Gold Standard.</strong> Since 1848, Swiss citizens have participated in <strong>over 600 national referenda and initiatives</strong>, voting 3&ndash;4 times per year on roughly 15 federal issues. Three instruments exist: mandatory referenda on constitutional amendments (requiring a double majority of popular vote <em>and</em> cantonal majority), optional referenda (triggered by 50,000 signatures within 100 days), and popular initiatives (100,000 signatures within 18 months).</p>
<p>Average turnout hovers around <strong>40&ndash;50%</strong>, but roughly <strong>90% of citizens</strong> participate at least once within any four-year period&mdash;suggesting selective engagement rather than apathy. Only ~11% of popular initiatives have been accepted, but their mere existence disciplines the legislature: the <em>threat</em> of a referendum forces broad compromise before bills pass. Freedom House score: <strong>96/100</strong>.</p>
</div>

<div class="callout green">
<p><strong><i class="fa-solid fa-people-group" style="color:var(--mr-green); margin-right:6px;"></i> Ireland's Citizens' Assemblies.</strong> The 2016&ndash;2018 Citizens' Assembly&mdash;99 randomly selected citizens with no politicians&mdash;deliberated for 19 months and recommended repealing the constitutional ban on abortion. The subsequent 2018 referendum passed with <strong>66.4% support</strong>. A study comparing the Assembly's deliberative quality with parliamentary committee debate found citizens demonstrated <strong>deeper cognitive grasp</strong> of the subject matter. Ireland has now held six consecutive sortition-based assemblies producing roughly 100 recommendations and three successful constitutional referenda&mdash;the world's leading empirical validation of epistemic democracy.</p>
</div>

