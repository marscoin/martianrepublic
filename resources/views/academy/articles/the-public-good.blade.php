<h3>Small-Group Governance in Practice</h3>

<p><strong>Hutterite colonies</strong>&mdash;~428 colonies, averaging ~90 members&mdash;split when reaching 125&ndash;150 members, approximately every 16 years. Assets are divided; members are separated (sometimes by lot). This is the purest real-world operationalization of Dunbar's number as governance policy.</p>

<p><strong>Kibbutzim</strong> practiced direct democracy through weekly General Assemblies where all members could vote on every topic. As they grew larger, attendance declined, hierarchy emerged, and many shifted from direct democracy to representative governance with referenda&mdash;the pattern predicted by Dunbar's theory. By the 2010s, two-thirds of kibbutz workforces were hired non-members, fundamentally altering the communal model.</p>

<div class="callout">
<p><strong><i class="fa-solid fa-skull-crossbones" style="color:var(--mr-cyan); margin-right:6px;"></i> Pirate Ship Governance</strong> (Golden Age, ~1690&ndash;1730) deserves particular attention. Bartholomew Roberts's Articles (c. 1720) encoded democratic principles nearly a century before the American Revolution: "Every man shall have an equal vote in affairs of moment." Captains were elected by crew vote and could be deposed by majority for cowardice, cruelty, or incompetence. A <strong>separation of powers</strong> existed: the captain held unrestricted authority only during battle, while the separately elected <strong>quartermaster</strong> resolved disputes, distributed loot, and determined punishments. Compensation was relatively egalitarian&mdash;captains received 2 shares versus 1 for regular crew. Crucially, pirate crews were naturally within Dunbar's range (80&ndash;200 men), and the democratic system worked because everyone knew everyone.</p>
