<p><strong>W.L. Gore &amp; Associates</strong> provides the corporate parallel. Founded in 1958, now a <strong>$3+ billion company</strong> with 10,000+ employees, Gore caps facilities at <strong>150 people with 150 parking spaces</strong>. "When people start parking on the grass, we know it's time to build a new plant," noted associate Bob Hen. Gore had no hierarchy, no formal chains of command, no titles. Leaders emerged naturally. The company arrived at this through trial and error <em>years before Dunbar published his research</em>. Malcolm Gladwell highlighted in <em>The Tipping Point</em>: "In order to be unified, Gore had to break itself up into semi-autonomous pieces."</p>

