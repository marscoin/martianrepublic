<p>Marscoin is the base layer. It's a proof-of-work cryptocurrency launched in 2014, with 123-second block times &mdash; a nod to the 1-2-3 countdown of a rocket launch. Like Bitcoin, it provides a censorship-resistant, permissionless, decentralized ledger. Unlike Bitcoin, it was designed from the beginning as the monetary foundation for a Martian civilization.</p>

<p>On Mars, Marscoin serves as the unit of account (prices are denominated in MARS), the medium of exchange (you pay for goods and services in MARS), and the settlement layer (all higher-layer protocols ultimately settle on the Marscoin blockchain). Its proof-of-work consensus means that no single party &mdash; no government, no corporation, no wealthy individual &mdash; can unilaterally control the money supply or censor transactions.</p>

