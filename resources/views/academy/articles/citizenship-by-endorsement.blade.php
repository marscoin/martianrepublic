<h2>Part V: The Arrival Hall &mdash; A Vision of Mars Immigration</h2>

<p>Theory is essential. But theory without vision is sterile. So let us paint the picture of what citizenship by endorsement looks like when it works &mdash; not in the abstract, but in the visceral, concrete reality of a person arriving on another planet.</p>

<h3>Arrival</h3>

<p>You step off the Starship ramp. Martian gravity &mdash; 38% of Earth's &mdash; makes your first steps feel like walking on a trampoline, and your inner ear takes a disorienting moment to recalibrate. The hatch seals behind you. The airlock cycles: a hiss of equalization, the amber status light turning green. The inner door opens into the Immigration Hall.</p>

<p>No lines. No forms. No bored government officials behind bulletproof glass. No armed guards. No queue snaking through velvet ropes for two hours while a fluorescent light buzzes overhead and a screen plays a silent loop of "Welcome to Our Country" propaganda.</p>

<p>Instead: a row of self-service kiosks, sleek and minimal, each displaying the Martian Republic seal. Your MR mobile app &mdash; the Martian Republic wallet you set up on Earth, months ago, while you were still training for the voyage &mdash; connects to the local Marscoin network via the kiosk's mesh relay. The kiosk display reads: <em>"Welcome to Mars. Your civic address has been detected. Please verify your identity."</em></p>

<p>You hold your phone to the kiosk camera. It shows your civic address &mdash; that pseudonymous string of characters that is, simultaneously, your voter registration, your bank account, and your proof of existence. You record a fresh liveness video: you, here, on Mars, visibly the same person who applied for General Public status from Earth six months ago. The kiosk pins the video to the local IPFS node. Your arrival transaction is broadcast: a GP_UPDATE from your civic address, with the new IPFS content identifier. Block confirmation: 123 seconds.</p>

<p>You are in.</p>

<p>You walk out of the Immigration Hall into the colony proper. The hab corridor stretches ahead, pressurized and warm, its walls lined with the colony's first attempts at art &mdash; a mural of Olympus Mons painted by a geologist who turned out to be a talented watercolorist. Your wallet already works: buy a coffee at the hab-caf&eacute; (treasury wallet, microtransaction, approximately 0.01 MARS, confirmed in two minutes). Check the Congress for active proposals (civic wallet, read-only until your endorsements come through). A notification appears on your phone: <em>"Signal Poll: Should the colony expand Greenhouse 3 before Greenhouse 4? Vote by Sol 14."</em></p>

<p>You can't vote yet &mdash; you need your endorsements. But you've already met three citizens during the voyage. They know you. They've lived with you in a metal tube for six months. Two of them endorsed you from the ship. A third plans to endorse you tonight. By Sol 3 of your life on Mars, you will be a full citizen of the Republic. Three days. No lawyers. No forms. No fees. No waiting list. Three people who know you, staking their reputation that you are real and that you belong.</p>

