<p>This is not negligence. It is not laziness. It is not a failure to implement proper security. It is <em>philosophy</em>. The Republic asserts, as a foundational principle, that the right to participate in governance and the right to hold and transact currency are <strong>fundamental human rights</strong> that should require the absolute minimum personal data exposure consistent with preventing fraud. You prove you are real. You prove you are unique. Everything else is your business.</p>

